  // Get districts for a specific state and language
  static List<String> getDistrictsForLanguage(String state, String languageCode) {
    final statesData = getStatesDataForLanguage(languageCode);
    final stateData = statesData.firstWhere(
      (element) => element['state'] == state,
      orElse: () => {"state": "", "districts": []},
    );
    return (stateData['districts'] as List).cast<String>();
  }

  // Get the English equivalent of a localized state name
  static String getEnglishStateName(String localizedState) {
    final hindiStates = _hindiStatesData;
    final englishStates = _englishStatesData;
    
    final hindiIndex = hindiStates.indexWhere((state) => state['state'] == localizedState);
    if (hindiIndex != -1) {
      return englishStates[hindiIndex]['state'] as String;
    }
    
    // If not found in Hindi, return the original (assuming it's already English)
    return localizedState;
  }

  // Get the English equivalent of a localized district name
  static String getEnglishDistrictName(String localizedDistrict, String state) {
    final hindiStates = _hindiStatesData;
    final englishStates = _englishStatesData;
    
    // Find the state index first
    final stateIndex = hindiStates.indexWhere((s) => s['state'] == state);
    if (stateIndex != -1) {
      final hindiDistricts = hindiStates[stateIndex]['districts'] as List;
      final englishDistricts = englishStates[stateIndex]['districts'] as List;
      
      final districtIndex = hindiDistricts.indexWhere((district) => district == localizedDistrict);
      if (districtIndex != -1) {
        return englishDistricts[districtIndex] as String;
      }
    }
    
    // If not found in Hindi, return the original (assuming it's already English)
    return localizedDistrict;
  }
