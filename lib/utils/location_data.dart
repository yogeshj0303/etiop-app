  // Get districts for a specific state based on current language
  static List<String> getDistricts(String state) {
    final statesData = _getStatesData();
    final stateData = statesData.firstWhere(
      (element) => element['state'] == state,
      orElse: () => {"state": "", "districts": []},
    );
    return (stateData['districts'] as List).cast<String>();
  }

